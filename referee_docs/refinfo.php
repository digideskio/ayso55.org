			<p>&nbsp; <a name="Common_Excuses_for_not_being_a_Referee">
			<span class="style23"><strong>Common Excuses for 
		not being a Referee</strong></span></a><span class="style23"><strong>&nbsp;
			</strong></span></p>
		<font color="#800000"><span class="style26">
		&nbsp;I don&#39;t know anything about the game!</span></font><p style="margin-top: 2px; margin-bottom: 2px" class="style25">&nbsp; 
		All we ask of you is a little of your time and we will train you. The 
		training can be done a little at a time or all at once, whichever is 
		more convenient for you. We will not ask you to do anything you do not 
		feel ready to try. We can guarantee you that, even if you have never 
		refereed a game in your life, with a few hours of training, you will 
		know more about the game of soccer than most Americans will ever know. 
		We can also guarantee you that, if only as spectator, your appreciation 
		and enjoyment of the game will increase.<p style="margin-top: 2px; margin-bottom: 2px" class="style25">
			&nbsp;<font color="#800000"><span class="style26">&nbsp; I don&#39;t have the 
			time!</span></font><p style="margin-top: 2px; margin-bottom: 2px" class="style25">&nbsp; 
		The referees are free to choose the games they can do, which can be 
		scheduled at your convenience, possibly just before or after the game 
		your child is playing.<p style="margin-top: 2px; margin-bottom: 2px" class="style25">
			&nbsp;<p style="margin-top: 2px; margin-bottom: 2px" class="style19">
		<font color="#800000"><span class="style26">
		&nbsp; I don&#39;t think I could put up with the behavior of some coaches 
			and spectators!</span></font><p style="margin-top: 2px; margin-bottom: 2px" class="style25">&nbsp; 
		We do not want our referees to put up with it! Most of the adults and 
		players involved in our program are here because we do not allow 
		unsportsmanlike behavior. Our region has strict policies governing such 
		behavior, and we are strongly committed to these policies. As a referee, 
		we can teach you how to deal with these situation in a dignified and 
		appropriate manner, and help us maintain the A.Y.S.O philosophy while 
		setting a good example for our impressionable young players.<p style="margin-top: 2px; margin-bottom: 2px" class="style25">
			&nbsp;<p style="margin-top: 2px; margin-bottom: 2px" class="style19">
		<font color="#800000">
            <span class="style26">
		&nbsp; I&#39;m not the right kind of person to be a referee, I&#39;d be 
			embarrassed!</span></font><p style="margin-top: 2px; margin-bottom: 2px" class="style25">&nbsp; 
		All you need is to like kids and like to see them have fun playing 
		soccer. We can teach you the rest. There is no need to feel embarrassed 
		because we are all volunteers and we all make mistakes, even our most 
		experiences referees. We would also make sure that you were always with 
		an experienced referee for support.&nbsp;<p style="margin-top: 2px; margin-bottom: 2px" class="style25">
			&nbsp;<p style="margin-top: 2px; margin-bottom: 2px" class="style19">
		<font color="#800000"><span class="style26">
		&nbsp; I can&#39;t afford the equipment, and besides, I&#39;m a woman and women 
			don&#39;t referee!</span></font><p style="margin-top: 2px; margin-bottom: 2px" class="style25">&nbsp; 
		Women certainly do referee, and make very good ones, too. This is 
		exactly why we would like you to volunteer, we would like to have more 
		women involved as referees. We provide most of the referee uniform and 
		equipment.&nbsp;<p style="margin-top: 2px; margin-bottom: 2px" class="style25">
			&nbsp;<p style="margin-top: 2px; margin-bottom: 2px" class="style19">
			<font color="#800000"><span class="style26">
		&nbsp; I don&#39;t think I could keep up with the players. Some of them are 
			pretty fast!</span></font><p style="margin-top: 2px; margin-bottom: 2px" class="style25">&nbsp; 
		Most of us can&#39;t keep up with them either. We give the upper division 
		games, with the faster players, to our fitter and more experienced 
		referees. We still have plenty of lower division games on smaller fields 
		where you would be able to keep up. Refereeing is also a good way to get 
		a little exercise and have fun at the same time.&nbsp;<p style="margin-top: 2px; margin-bottom: 2px" class="style25">
			&nbsp;<p style="margin-top: 2px; margin-bottom: 2px" class="style19">
		<font color="#800000"><span class="style26">
		&nbsp; I don&#39;t think I could handle the responsibilities of the referee!</span></font><p style="margin-top: 2px; margin-bottom: 2px" class="style25">&nbsp; 
		In time you will develop the confidence to referee, but even if you 
		don&#39;t, you could provide vital assistance to the referee by being an 
		assistant referee. We have several qualified referees who only work as 
		assistant referees.<p style="margin-top: 2px; margin-bottom: 2px" class="style25">
			&nbsp;<font color="#800000"><span class="style26">I&#39;m too young!</span></font><p style="margin-top: 2px; margin-bottom: 2px" class="style20">
		<span class="style26">&nbsp; All you have to be is 12 years old. We will 
		teach you how to referee games for the younger divisions. As a general 
		rule, referees only do games for players younger than they are. We have 
		a special Youth Referee Administrator and monitors on the fields to 
		assist our younger referees.</span><span class="style23">&nbsp;</span><p style="margin-top: 2px; margin-bottom: 2px" class="style25">
			&nbsp;<p style="margin-top: 2px; margin-bottom: 2px" class="style25">&nbsp;<p style="margin-top: 2px; margin-bottom: 2px" class="style25">&nbsp;
