			<span class="style4">AYSO philosophy:</span>
            <ul>
              <li class="style16">Everybody plays</li>
              <li class="style16">Open registration</li>
              <li class="style16">Balanced teams</li>
              <li class="style16">Positive coaching</li>
              <li class="style16">Good sportsmanship</li>
            </ul>
            <p class="style4">The following 
            is an expansion on these ideas.</p>
            <p class="style4">1. The coach is the most 
            important AYSO person for the kids:&nbsp;</p>
            <ul>
              <li class="style16">You have the most contact.&nbsp;</li>
              <li class="style16">You make it or break it for the 
              kids.&nbsp;</li>
              <li class="style16">The kids will have lasting 
              memories about how you treat them.</li>
            </ul>
            <p class="style4">2. Control the sidelines.&nbsp;</p>
            <ul>
              <li class="style16">You are responsible for your own 
              conduct, the conduct of assistant coaches, players and parents. 
              Most of them take their cues from you. If you are yelling at the 
              refs and players, they will feel that it is ok to do so. You need 
              to set the example that all of the others will follow. Referees 
              don't point out your coaching mistakes. So, do not vocalize 
              referee errors.</li>
            </ul>
            <p class="style4">3. How invested are you in the 
            team winning?&nbsp;</p>
            <ul>
              <li class="style16">It is not about you winning. It 
              is about the kids having fun and developing their skills. When you 
              see a coach screaming at the players, look to see if the kids 
              being yelled at are having any fun.</li>
            </ul>
            <p class="style4">4. How to coach a game.&nbsp;</p>
            <ul>
              <li class="style16">Stay in the designated coaching 
              area. As hard as it is, remain relatively quiet.&nbsp;</li>
              <li class="style16">Don't choreograph the play. Let 
              them play the game.&nbsp;</li>
              <li class="style16">Do 80% of your coaching at 
              practice.&nbsp;</li>
            </ul>
            </center>
            <p align="left" class="style4">Your conduct, 
            mandated by AYSO, is to be:&nbsp;</p>
            <blockquote>
              <ul>
                <li>
                <p align="left" class="style16">positive</li>
                <li>
                <p align="left" class="style16">instructional</li>
                <li>
                <p align="left" class="style16">encouraging.&nbsp;</li>
              </ul>
            </blockquote>
            <ul>
              <li>
              <p align="left"><font class="style16">Don't shout, 
              &quot;SHOOT!, SHOOT!&quot; or &quot;KICK IT, KICK IT!&quot; or &quot;WHAT ARE YOU DOING!?!&quot; 
              Offer minimal instruction to players who are away from the ball.</font></li>
              <li>
              <p align="left"><font class="style16">Your objective must 
              be to facilitate the development of players to play attractive 
              soccer. Attractive soccer is the skillful / cooperative actions of 
              ALL players on a team to maintain possession of the ball with 
              accurate passes to each other using ALL areas of the field in a 
              concerted effort to maneuver the ball toward the opponents goal 
              and SCORE. If we are ultimately working toward well-paced ball 
              circulation, then 1st touch (reception) ball control is our 
              highest priority. Formulate your coaching methods with that 
              technical skill as the primary objective. A well executed 
              reception, turn and pass has much more value than a ball kicked 
              with reckless abandon and no target in mind.</font></li>
              <li>
              <p align="left" class="style16">Playing soccer is 
              FUN. Children love free-play. It enhances the development of 
              individual and cooperative DECISION MAKING, crucial to the flow of 
              the game. Adults can help this development by not shouting 
              negatively from the sideline. Shouting divides the player's 
              attention away from the action on the field. Allow the children to 
              make their own on-the-field decisions.</li>
            </ul>
